def _safe_float(val) -> float:
    """Safely convert a value to float, returning 0 on failure."""
    if val is None:
        return 0.0
    try:
        s = str(val).replace(",", "").replace("#N/A", "0").strip()
        return float(s) if s else 0.0
    except (ValueError, TypeError):
        return 0.0


def _read_xls(filepath: Path) -> list[list]:
    """Read .xls file using xlrd. Returns list of rows (each row = list of cell values)."""
    try:
        import xlrd
    except ImportError:
        logger.error("xlrd not installed. Run: pip install xlrd")
        return []

    try:
        wb = xlrd.open_workbook(str(filepath))
        ws = wb.sheet_by_index(0)
        rows = []
        for row_idx in range(ws.nrows):
            row = []
            for col_idx in range(ws.ncols):
                cell = ws.cell(row_idx, col_idx)
                # Handle xlrd date cells
                if cell.ctype == xlrd.XL_CELL_DATE:
                    try:
                        dt_tuple = xlrd.xldate_as_tuple(cell.value, wb.datemode)
                        row.append(datetime(*dt_tuple[:6]))
                    except Exception:
                        row.append(cell.value)
                else:
                    row.append(cell.value)
            rows.append(row)
        return rows
    except Exception as e:
        logger.debug(f"xlrd could not read {filepath.name}: {e}")
        return []


def _read_xlsx(filepath: Path) -> list[list]:
    """Read .xlsx file using openpyxl. Returns list of rows."""
    try:
        import openpyxl
    except ImportError:
        logger.error("openpyxl not installed. Run: pip install openpyxl")
        return []

    try:
        wb = openpyxl.load_workbook(filepath, read_only=True, data_only=True)
        ws = wb.active
        if not ws:
            wb.close()
            return []
        rows = []
        for row in ws.iter_rows(values_only=True):
            rows.append(list(row))
        wb.close()
        return rows
    except Exception as e:
        logger.debug(f"openpyxl could not read {filepath.name}: {e}")
        return []


def _parse_date(val) -> datetime:
    """Parse various date formats from GasUp reports."""
    if isinstance(val, datetime):
        return val
    if val is None:
        return datetime.now()
    s = str(val).strip()
    # Try common formats
    for fmt in ("%Y-%m-%d", "%d/%m/%Y", "%Y-%m-%d %H:%M:%S", "%d/%m/%Y %H:%M:%S",
                "%Y-%m-%dT%H:%M:%S", "%m/%d/%Y"):
        try:
            return datetime.strptime(s, fmt)
        except ValueError:
            continue
    try:
        return datetime.fromisoformat(s)
    except (ValueError, TypeError):
        return datetime.now()


