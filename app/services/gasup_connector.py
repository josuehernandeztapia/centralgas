"""
GasUp Connector — Fase 2

Abstract interface for consuming GasUp transaction data in the Central Gas
AI Agent (retention engine, market intelligence, operational dashboards).

Data flows INTO this connector from two sources:
  1. Fly.io webhook server (gasup-webhook.ts) forwards transactions
  2. Local file watcher picks up Excel reports dropped in a folder

Data flows OUT to:
  - retention engine (build_client_profiles, alert on inactivity)
  - market_loader (enrich vehicle profiles with real consumption)
  - operational dashboards (station utilization, revenue tracking)

Architecture:
  GasUp BackOffice
    → gasup-webhook.ts (Fly.io) → Airtable TABLE_RECAUDO (financial engine)
    → gasup_connector.py (this) → retention engine (central-gas-agent)

The connector is source-agnostic: it consumes GasUpTransaction objects
regardless of whether they came from a webhook, Excel upload, or CSV.

Integration with market_loader.py:
  - classify_placa() identifies the vehicle segment
  - SEGMENT_PARAMS provides expected consumption for anomaly detection
  - build_existing_profiles() can be enriched with real GasUp data
"""

